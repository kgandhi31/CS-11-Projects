package OOP5_BeastMaster;

public class Contestants {

//instance variables    
    public String fullName;
    public int age;
    public String job;
    public String country;
    public int episode;
    public String firstName;
    public String gender;
    public int points;

//constructor methods
    public Contestants(String fullN, int a, String j, String c, int e, String firstN, String g, int p) {
        fullName = fullN;
        age = a;
        job = j;
        country = c;
        episode = e;
        firstName = firstN;
        gender = g;
        points = p;
    }
}
